# Utilizando operador logica de igualadade
print(f"5 é igual a 5? {5 == 5}\n5 é igual a '5'? {5 == '5'}\n")

# Utilizando operador logica de diferença
print(f"5 é diferente de 10? {5 != 10}\n5 é difefente de 5? {5 != 5}\n")

# Utilizando operador logica de maior que
print(f"10 é maior que 5? {10 > 5}\n5 é maior que 10? {5 > 10}\n")

# Utilizando operador logica de menor que
print(f"5 é menor que 10? {5 < 10}\n5 é menor que 5? {5 < 5}\n")

# Utilizando operador logica de maior, igual que
print(f"10 é maior que 5? {10 >= 5}\n10 é maior que 10? {10 >= 10}\n5 é maior que 10? {5 >= 10}\n")

# Utilizando operador logica de menor, igual que
print(f"5 é maior que 10? {5 <= 10}\n10 é maior que 10? {10 <= 10}\n10 é maior que 5? {10 <= 5}\n")