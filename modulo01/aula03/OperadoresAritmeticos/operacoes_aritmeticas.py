a = 10
b = 2

soma = a + b # resultado: 15
subtracao = a - b # resultado: 5
multiplicacao = a * b # resultado: 50
divisao = a / b # resultado: 2.5
divisao_inteira = a // b # resultado: 2 
modulo = a % b # resultado: 0
exponenciacao = a**2 # resultado: 100

print(f'Soma: {a} + {b} = {soma}\nSubtracao: {a} - {b} = {subtracao}\n' + 
      f'Multiplicacao: {a} * {b} = {multiplicacao}\n' + 
      f'Divisao: {a} / {b} = {divisao}\nDivisao inteira: {a} // {b} = {divisao_inteira}\n' + 
      f'Modulo: {a} % {b} = {modulo}\nExponenciacao: {a} ** {b} = {soma}')