# Definindo uma variável inicial
saldo = 100

# Usando o operador += para somar
saldo += 50    # Equivalente a: saldo = saldo + 50
print(f"Saldo após o depósito: {saldo}")  # Saída: 150

# Usando o operador -= para subtrair
saldo -= 25    # Equivalente a: saldo = saldo - 25
print(f"Saldo após o saque: {saldo}")    # Saída: 125

# Usando o operador *= para multiplicar
estoque = 10
estoque *= 2   # Equivalente a: estoque = estoque * 2
print(f"Estoque dobrado: {estoque}")      # Saída: 20

# Usando o operador /= para dividir
total_vendas = 500
total_vendas /= 2  # Equivalente a: total_vendas = total_vendas / 2
print(f"Total de vendas dividido: {total_vendas}")  # Saída: 250.0



